export const getMonthNumber = (month: string) => {
  switch (month) {
    case 'enero':
      return '01';
    case 'febrero':
      return '02';
    case 'marzo':
      return '03';
    case 'abril':
      return '04';
    case 'mayo':
      return '05';
    case 'junio':
      return '06';
    case 'julio':
      return '07';
    case 'agosto':
      return '08';
    case 'septiembre':
      return '09';
    case 'octubre':
      return '10';
    case 'noviembre':
      return '11';
    case 'diciembre':
      return '12';
  }
};
